package model;

/**
 *
 * @author Clara
 */
public class UsuarioModel {
    
    private Integer id;
    private String nome;

    public UsuarioModel(Integer id, String nome) {
        this.id = id;
        this.nome = nome;
    }

    public UsuarioModel() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
}
